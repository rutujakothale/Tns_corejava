package day_one;

public class Chardemo {
	public static void main(String[] args) {
		
		//single char
		char ch='a';
		System.out.println(ch);
		
		//
		char ch1=64;
		System.out.println(ch1);
		
		char ch2='\u00A7';
		System.out.println(ch2);
		
		char ch3='\u20AC';
		System.out.println(ch3);
		
		char ch4='A';
		System.out.println(ch4);
	}

}
